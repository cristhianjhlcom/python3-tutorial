## Contador de Palabras
# Desarrolla un programa que cuente cuántas palabras
# hay en una cadena de texto ingresada por el usuario.
# Debes considerar que las palabras están separadas por espacios.
## TODOS
# 1. Solicitar al usuario que ingrese una cadena de texto.
# 2. Inicializar una variable para llevar un registro del número de palabras, comenzando en 0.
# 3. Iterar a través de cada carácter en la cadena.
# 4. Para cada carácter, verificar si es un espacio en blanco (espacio, tabulación o salto de línea).
# 5. Si encontramos un espacio en blanco, incrementar el contador de palabras en 1.
# 6. Continuar el proceso hasta que hayamos recorrido toda la cadena.
# 7. Al final, mostrar el número de palabras.

